package com.common.util;


public class AlgorithmParameter {
    public int ants;
    public int cycles;
    public int beta;
    public double intdiv;
    public double pherevp;
    public int busCapacity;

    public int getBusCapacity() {
        return busCapacity;
    }

    public void setBusCapacity(int busCapacity) {
        this.busCapacity = busCapacity;
    }

    public int getAnts() {
        return ants;
    }

    public void setAnts(int ants) {
        this.ants = ants;
    }

    public int getBeta() {
        return beta;
    }

    public void setBeta(int beta) {
        this.beta = beta;
    }

    public int getCycles() {
        return cycles;
    }

    public void setCycles(int cycles) {
        this.cycles = cycles;
    }

    public double getIntdiv() {
        return intdiv;
    }

    public void setIntdiv(double intdiv) {
        this.intdiv = intdiv;
    }

    public double getPherevp() {
        return pherevp;
    }

    public void setPherevp(double pherevp) {
        this.pherevp = pherevp;
    }

    
    

}
